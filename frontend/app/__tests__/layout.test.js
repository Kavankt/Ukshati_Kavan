import { metadata } from "../layout"; // Only testing metadata

describe("RootLayout Metadata", () => {
  beforeEach(() => {
    document.title = "";
  });

  test("sets the correct metadata title", () => {
    document.title = metadata.title;
    expect(document.title).toBe("Ukshati");
  });

  test("ensures font classes are added manually", () => {
    document.body.classList.add("custom-font");
    expect(document.body.classList.contains("custom-font")).toBe(true);
  });
});




















































































































































































































